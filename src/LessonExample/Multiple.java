package LessonExample;

public class Multiple {
    private Integer A;
    private Integer B;

    public Multiple(Integer a, Integer b) {
        A = a;
        B = b;
    }

    public Integer getA() {
        return A;
    }

    public void setA(Integer a) {
        A = a;
    }

    public Integer getB() {
        return B;
    }

    public void setB(Integer b) {
        B = b;
    }
}
